package com.buaa.hr.entity;

public class Dept {
	private int deptId;
	private String deptName;
	private int deptLevel;//部门级别
	private int superiorDeptId;
	
	
	
	public int getDeptId() {
		return deptId;
	}
	public void setDeptId(int deptId) {
		this.deptId = deptId;
	}
	public String getDeptName() {
		return deptName;
	}
	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}
	public int getDeptLevel() {
		return deptLevel;
	}
	public void setDeptLevel(int deptLevel) {
		this.deptLevel = deptLevel;
	}
	public int getSuperiorDeptId() {
		return superiorDeptId;
	}
	public void setSuperiorDeptId(int superiorDeptId) {
		this.superiorDeptId = superiorDeptId;
	}
	
	
	
	
	
	
}
